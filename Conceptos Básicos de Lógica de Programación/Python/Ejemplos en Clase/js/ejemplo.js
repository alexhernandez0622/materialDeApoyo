// Crear una funcíon para sumar 2 números

// Declarar la función
function sumarNumeros(num1 , num2) {
    // Inicializar una variable que reciba la formula de la sumatoria
    let resultado = num1 + num2;
    // Retornar el resultado
    return resultado;
}
// Invocar la funcion y imprimimos el resultado
console.log(sumarNumeros(5, 3));
