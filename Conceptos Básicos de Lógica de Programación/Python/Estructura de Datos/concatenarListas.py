##  Concatenar dos listas de números y luego imprimir la lista resultante.

# Dos listas de números
lista1 = [1, 2, 3]
lista2 = [4, 5, 6]

# Concatenar las dos listas usando el operador de suma
lista_concatenada = lista1 + lista2

# Imprimir la lista concatenada
print("Lista concatenada:", lista_concatenada)
