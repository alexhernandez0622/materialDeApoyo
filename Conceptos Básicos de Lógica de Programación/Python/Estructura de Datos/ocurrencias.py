##  Contar cuántas veces aparece el número 3 en una lista dada.
# Lista de números
numeros = [1, 3, 5, 3, 7, 9, 3, 3, 10]

# Usar el método count() para contar las ocurrencias del número 3 en la lista
ocurrencias_de_3 = numeros.count(3)

# Imprimir el resultado
print("El número 3 aparece", ocurrencias_de_3, "veces en la lista.")
