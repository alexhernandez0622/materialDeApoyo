## Crear un diccionario con claves numéricas y sumar todos sus valores.

# Crear un diccionario con claves numéricas y sus respectivos valores
valores = {1: 10, 2: 20, 3: 30, 4: 40}

# Sumar todos los valores del diccionario usando sum() y values()
suma_total = sum(valores.values())

# Imprimir la suma total de los valores
print("La suma de todos los valores en el diccionario es:", suma_total)
