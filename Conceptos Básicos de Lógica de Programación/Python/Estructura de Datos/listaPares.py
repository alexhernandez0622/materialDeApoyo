## Crear una lista que contenga los números pares del 2 al 20 y luego imprimirla.
# Crear una lista vacía para almacenar los números pares
numeros_pares = []

# Usar un bucle for para generar los números pares del 2 al 20
for i in range(2, 21, 2):
    numeros_pares.append(i)  # Agregar cada número par a la lista

# Imprimir la lista de números pares
print("Lista de números pares del 2 al 20:", numeros_pares)
