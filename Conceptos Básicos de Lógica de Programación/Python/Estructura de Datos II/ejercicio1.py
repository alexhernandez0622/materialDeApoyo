# Ejercicio 1: Creación de una tupla y acceso a sus elementos

# Se crea una tupla con diferentes tipos de datos
mi_tupla = (10, "Python", 3.14, True)

# Acceso al primer elemento de la tupla
print("El primer elemento de la tupla es:", mi_tupla[0])

# Acceso al último elemento de la tupla
print("El último elemento de la tupla es:", mi_tupla[2])

