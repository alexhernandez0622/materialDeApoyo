# Ejercicio 3: Creación de un diccionario y acceso a sus elementos

# Creación de un diccionario con pares clave-valor
mi_diccionario = {
    "nombre": "Ana",
    "edad": 25,
    "ciudad": "Madrid"
}

# Acceso a un valor del diccionario mediante su clave
print("Nombre:", mi_diccionario["nombre"])

# Acceso a otro valor del diccionario mediante su clave
print("Ciudad:", mi_diccionario["ciudad"])

# Si intentamos acceder a una clave que no existe, se producirá un error
