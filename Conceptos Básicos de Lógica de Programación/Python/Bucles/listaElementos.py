# Ejercicio 3: Imprimir todos los elementos de una lista utilizando un bucle for.

# Definir una lista de frutas.
frutas = ["manzana", "banana", "cereza", "naranja", "pera"]

# Utilizar un bucle for para iterar sobre cada elemento de la lista.
for fruta in frutas:
    print(fruta)  # Imprimir cada fruta en la consola.
