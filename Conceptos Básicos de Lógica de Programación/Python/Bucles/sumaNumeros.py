# Ejercicio 2: Calcular la suma de los números del 1 al 100.

# Inicializar una variable para almacenar la suma.
suma = 0

# Utilizar un bucle for para sumar los números del 1 al 100.
for i in range(1, 6):
    suma += i  # Sumar cada número a la variable suma.

# Mostrar el resultado de la suma.
print(f"La suma de los números del 1 al 10 es: {suma}")
