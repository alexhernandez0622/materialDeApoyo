# Comprobar si un número es par o impar

# Definir un número
numero = 4  # Número a comprobar

# Comprobar si el número es par o impar
if numero % 2 == 0:  # Si el resto de dividir el número por 2 es 0, es par
    print("El número es par.")  # Mensaje si el número es par
else:
    print("El número es impar.")  # Mensaje si el número es impar
