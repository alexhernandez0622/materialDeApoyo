# Ejercicio 6: Añadir un nuevo elemento a un diccionario.

# Crear un diccionario con información de un libro.
libro = {
    "titulo": "Cien años de soledad",
    "autor": "Gabriel García Márquez"
}

# Añadir el año de publicación al diccionario.
libro["ano_publicacion"] = 1967

# Mostrar el diccionario actualizado.
print("Diccionario del libro actualizado:", libro)
