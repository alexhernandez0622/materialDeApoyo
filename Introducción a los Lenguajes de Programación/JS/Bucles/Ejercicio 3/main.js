/*Inicializa un array vacío llamado "array" y utiliza un bucle "for" para llenarlo con números del 1 */

// Se declara una variable llamada "array" y se inicializa con un array vacío.
// Se crea un array vacío llamado "array".
let array = [];

// Se utiliza un bucle "for" para llenar el array con números del 1 al 10.
for (let i = 1; i <= 10; i++) {
    // Cada número del bucle se agrega al final del array.
    array.push(i);
}

// Se imprime el contenido del array en la consola.
console.log(array);
