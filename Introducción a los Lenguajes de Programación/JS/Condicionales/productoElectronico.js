/*Crear un programa que verifique si el producto pertenece a la categoría*/

// Ejemplo de operador OR (||)
const producto = "Laptop";
const categoria = "Electrónica";

// Verificar si el producto es una laptop o pertenece a la categoría de electrónica
if (producto === "Laptop" || categoria === "Electrónica") {
  console.log("El producto es una laptop o pertenece a la categoría de electrónica.");
} else {
  console.log("El producto no cumple con los criterios especificados.");
}
